public class TipoChar {
    public static void main(String args[]){
        char miCaracter = 'a';
        
        System.out.println("");
        System.out.println("----------------------");
        System.out.println("CHAR   CHAR   CHAR");
        
        System.out.println("miCaracter: " + miCaracter);
        
        char varCharCode = '\u0021';
        System.out.println("varChar: " + varCharCode);
        
        char varCharDecimal = 33;
        System.out.println("varCharDecimal: " +varCharDecimal);
        
        char varCharSimbolo = '!';
        System.out.println("varCharSimbolo: " + varCharSimbolo);
                
        System.out.println("");
        System.out.println("----------------------");
        System.out.println("VAR   VAR   VAR");
        
        System.out.println("miCaracter: " + miCaracter);
        
        var varCharCode1 = '\u0021';
        System.out.println("varChar: " + varCharCode1);
        
        var varCharDecimal1 = 33;
        System.out.println("varCharDecimal: " +varCharDecimal1);
        
        var varCharSimbolo1 = '!';
        System.out.println("varCharSimbolo: " + varCharSimbolo1);
               
    }
    
    
}
